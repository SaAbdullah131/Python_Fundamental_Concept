# Slicing String 
myString = "Hello, World";
print(myString[2:5]);  # Get the character from position 2 to position 5(not included);

# Slice from the Start 
b = "Hello,Europe";
print(b[:5]) # Slice from start that mean 0 to 5(not included);

#Slice to The End
ToTheEnd = 'Hello, Programming' ;
print(ToTheEnd[2:]); # start from 2 position and go to the end

# Negative Indexing 

Negative = "Hello,Python";
print(Negative[-5:-2]); # "Y" in "Python"(position -5) To,but not included: "o" in "Python"(position -2);
